// 提供一个简单的工具函数来处理模板变量替换
export const processTemplate = (templateKey, variables = {}) => {
  let content = promptTemplates[templateKey] || '';
  
  // 替换变量，格式为 {variableName}
  for (const [key, value] of Object.entries(variables)) {
    const placeholder = new RegExp(`\\{${key}\\}`, 'g');
    content = content.replace(placeholder, value);
  }
  
  return content;
}; 